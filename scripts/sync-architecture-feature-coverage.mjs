import fs from "node:fs";
import path from "node:path";

const root = process.cwd();

function readText(relativePath) {
  return fs.readFileSync(path.join(root, relativePath), "utf8");
}

function writeText(relativePath, content) {
  fs.writeFileSync(path.join(root, relativePath), content);
}

function parseCsv(text) {
  const rows = [];
  let row = [];
  let cell = "";
  let quoted = false;
  for (let i = 0; i < text.length; i += 1) {
    const ch = text[i];
    const next = text[i + 1];
    if (quoted) {
      if (ch === "\"" && next === "\"") {
        cell += "\"";
        i += 1;
      } else if (ch === "\"") {
        quoted = false;
      } else {
        cell += ch;
      }
      continue;
    }
    if (ch === "\"") quoted = true;
    else if (ch === ",") {
      row.push(cell.trim());
      cell = "";
    } else if (ch === "\n") {
      row.push(cell.trim());
      if (row.some(Boolean)) rows.push(row);
      row = [];
      cell = "";
    } else if (ch !== "\r") {
      cell += ch;
    }
  }
  if (cell.length || row.length) {
    row.push(cell.trim());
    if (row.some(Boolean)) rows.push(row);
  }
  return rows;
}

function csvEscape(value) {
  const text = String(value ?? "");
  if (/[",\n]/.test(text)) return `"${text.replace(/"/g, "\"\"")}"`;
  return text;
}

function loadCsv(relativePath) {
  const rows = parseCsv(readText(relativePath));
  const header = rows[0];
  const records = rows.slice(1).map((values) => {
    const out = {};
    header.forEach((name, idx) => {
      out[name] = values[idx] ?? "";
    });
    return out;
  });
  return { header, records };
}

function toCsv(header, records) {
  const lines = [header.join(",")];
  for (const row of records) {
    lines.push(header.map((name) => csvEscape(row[name] ?? "")).join(","));
  }
  return `${lines.join("\n")}\n`;
}

function toTitle(value) {
  return String(value || "")
    .split(/[-_]/)
    .filter(Boolean)
    .map((part) => part[0].toUpperCase() + part.slice(1))
    .join(" ");
}

function nextAutoFeatureId(records) {
  const nums = records
    .map((r) => String(r.id || ""))
    .filter((id) => id.startsWith("FEAT-AUTO-"))
    .map((id) => Number(id.replace("FEAT-AUTO-", "")))
    .filter((n) => Number.isFinite(n));
  const n = nums.length ? Math.max(...nums) + 1 : 1;
  return `FEAT-AUTO-${String(n).padStart(4, "0")}`;
}

function main() {
  const featuresCsv = loadCsv("docs/architecture/nodes/features.csv");
  const apiCsv = loadCsv("docs/architecture/nodes/api_routes.csv");

  const featureByModule = new Map();
  const featureById = new Map();
  for (const f of featuresCsv.records) {
    if (f.module && !featureByModule.has(f.module)) featureByModule.set(f.module, f);
    if (f.id) featureById.set(f.id, f);
  }

  const modulesNeedingFeature = new Set(
    apiCsv.records
      .filter((api) => String(api.id || "").startsWith("API-AUTO-"))
      .map((api) => api.module)
      .filter(Boolean)
      .filter((moduleName) => !featureByModule.has(moduleName))
  );

  const createdFeatures = [];
  for (const moduleName of [...modulesNeedingFeature].sort()) {
    const id = nextAutoFeatureId(featuresCsv.records);
    const feature = {
      id,
      name: `${toTitle(moduleName)} Coverage Expansion`,
      type: "feature",
      status: "implemented",
      layer: "full-stack",
      module: moduleName,
      feature: `${moduleName}-coverage`,
      description: `Auto-generated feature grouping for ${moduleName} API coverage expansion.`,
      file_path: "docs/architecture/architecture-evidence-system.md",
      related_files: `src/modules/${moduleName}/${moduleName}.routes.ts`,
      parent_id: "",
      child_ids: "",
      depends_on: "DOC-ARCH-EVIDENCE-SYSTEM",
      used_by: "AGENT-COORDINATOR",
      ui_related: "",
      api_related: "",
      database_related: "",
      tests_related: "TEST-API-LOCAL",
      docs_related: "DOC-ARCH-EVIDENCE-SYSTEM",
      agent_related: "AGENT-COORDINATOR",
      risk_level: "medium",
      completion_percent: "50",
      last_verified_at: "",
      verification_status: "implemented_not_verified",
      notes: "Auto-generated by feature coverage sync; refine links and status per module evidence.",
      tags: "#feature #auto-scaffold #coverage-expansion"
    };
    featuresCsv.records.push(feature);
    featureByModule.set(moduleName, feature);
    featureById.set(id, feature);
    createdFeatures.push(id);
  }

  let apiUpdated = 0;
  for (const api of apiCsv.records) {
    if (!String(api.id || "").startsWith("API-AUTO-")) continue;
    const moduleFeature = featureByModule.get(api.module || "");
    if (!moduleFeature) continue;
    const before = JSON.stringify(api);
    api.parent_id = api.parent_id || moduleFeature.id;
    if (!api.feature || api.feature === "coverage-expansion") api.feature = moduleFeature.feature;
    if (JSON.stringify(api) !== before) apiUpdated += 1;
  }

  writeText("docs/architecture/nodes/features.csv", toCsv(featuresCsv.header, featuresCsv.records));
  writeText("docs/architecture/nodes/api_routes.csv", toCsv(apiCsv.header, apiCsv.records));

  const report = {
    synchronizedAt: new Date().toISOString(),
    createdFeatures: createdFeatures.length,
    createdFeatureIds: createdFeatures,
    apiRowsUpdated: apiUpdated
  };
  writeText("docs/status/architecture-feature-coverage-sync-report.json", `${JSON.stringify(report, null, 2)}\n`);
  console.log(JSON.stringify(report, null, 2));
}

main();
